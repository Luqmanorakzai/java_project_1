public class IterationOrLooping {
    public static void main(String[] agr) {

        // for loop
        int i;
        for (i = 1; i <= 10; i++) {
            System.out.println("value of i " + i);
        }

        System.out.println(".....................");
        // while loop
        int j = 1;
        while (j <= 10) {
            System.out.println("value of j " + j);
            j++;
        }

        System.out.println(".....................");
        // do while loop
        int k = 1;
        do {
            System.out.println("value of k " + k);
            k++;
        } while (k <= 10);


    }
}
